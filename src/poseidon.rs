fn get_round_constants(t: usize) -> Vec<Fp> {
    match t {
        3 => C_ARITY_2
            .iter()
            .map(|s| Fp::from_str_vartime(s).unwrap())
            .collect(),
        4 => C_ARITY_3
            .iter()
            .map(|s| Fp::from_str_vartime(s).unwrap())
            .collect(),
        7 => C_ARITY_6
            .iter()
            .map(|s| Fp::from_str_vartime(s).unwrap())
            .collect(),
        _ => panic!("Unsupported arity t = {}", t),
    }
}

#[inline(always)]
pub fn sigma(x: Fp) -> Fp {
    let x2 = x.square();
    let x4 = x2.square();
    x * x4 // x^5
}

pub fn poseidon(inputs: &[Fp]) -> Fp {
    let t = inputs.len() + 1;

    let (rounds_p, m): (usize, &[&[&str]]) = match t {
        3 => (57, &M_ARITY_2[..]), // for 2 inputs
        4 => (56, &M_ARITY_3[..]), // for 3 inputs
        7 => (63, &M_ARITY_6[..]), // for 6 inputs
        _ => panic!("Unsupported input count: {}", t - 1),
    };

    const ROUNDS_F: usize = 8;
    let round_constants: Vec<Fp> = get_round_constants(t);

    let mut state = vec![Fp::ZERO];
    state.extend_from_slice(inputs);

    for r in 0..(ROUNDS_F + rounds_p) {
        // Add round constants
        for i in 0..t {
            state[i] += round_constants[r * t + i];
        }

        // Apply S-box
        if r < ROUNDS_F / 2 || r >= ROUNDS_F / 2 + rounds_p {
            for i in 0..t {
                state[i] = sigma(state[i]);
            }
        } else {
            state[0] = sigma(state[0]);
        }

        // MDS matrix multiplication
        let mut new_state = vec![Fp::ZERO; t];
        for i in 0..t {
            for j in 0..t {
                let m_ij = Fp::from_str_vartime(m[i][j]).unwrap();
                new_state[i] += m_ij * state[j];
            }
        }

        state = new_state;
    }

    state[0]
}

pub fn poseidon2(a: Fp, b: Fp) -> Fp {
    poseidon(&[a, b])
}

pub fn poseidon3(a: Fp, b: Fp, c: Fp) -> Fp {
    poseidon(&[a, b, c])
}

pub fn poseidon6(a: Fp, b: Fp, c: Fp, d: Fp, e: Fp, f: Fp) -> Fp {
    poseidon(&[a, b, c, d, e, f])
}
